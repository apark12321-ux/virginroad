const buildPosts = (): Post[] => {
  const postsList: Post[] = [];
  let currentMonth = 4;
  let currentDate = 1;

  let finIdx = 0;
  let appIdx = 0;
  let wedIdx = 0;

  const getCategoryImage = (category: "신혼금융" | "신혼가전" | "결혼준비"): string => {
    let id = "";
    if (category === "신혼금융") {
      id = FINANCE_IMAGES[finIdx % FINANCE_IMAGES.length];
      finIdx++;
    } else if (category === "신혼가전") {
      id = APPLIANCE_IMAGES[appIdx % APPLIANCE_IMAGES.length];
      appIdx++;
    } else {
      id = WEDDING_IMAGES[wedIdx % WEDDING_IMAGES.length];
      wedIdx++;
    }
    return `https://images.unsplash.com/photo-${id}?auto=format&fit=crop&q=80&w=800`;
  };

  for (let i = 0; i < 61; i++) {
    const dateStr = `2026-0${currentMonth}-${currentDate < 10 ? '0' + currentDate : currentDate}`;
    
    if (i < ORIGINAL_17.length) {
      const orig = ORIGINAL_17[i];
      postsList.push({
        id: orig.id,
        title: orig.title,
        excerpt: orig.excerpt,
        category: orig.category,
        author: "버진로드 편집부",
        date: dateStr,
        image: getCategoryImage(orig.category),
        readTime: orig.readTime,
        hashtags: orig.hashtags,
        content: ""
      });
    } else {
      const newIdx = i - ORIGINAL_17.length;
      const catSelector = newIdx % 3;
      const catData = NEW_TOPICS[catSelector];
      const itemIdx = Math.floor(newIdx / 3) % catData.titles.length;
      
      const title = catData.titles[itemIdx];
      const excerpt = catData.excerpts[itemIdx];
      const tags = catData.tags[itemIdx];
      const category = catData.category;
      const prefix = category === "신혼금융" ? "fin" : category === "신혼가전" ? "app" : "wed";
      
      postsList.push({
        id: `${prefix}-new-${newIdx}`,
        title,
        excerpt,
        category,
        author: "버진로드 편집부",
        date: dateStr,
        image: getCategoryImage(category),
        readTime: "5분",
        hashtags: tags,
        content: ""
      });
    }
    
    currentDate++;
    if (currentMonth === 4 && currentDate > 30) {
      currentMonth = 5;
      currentDate = 1;
    }
  }

  return postsList.map(post => {
    post.content = generateCustomContent(post);
    return post;
  });
};

export const MOCK_POSTS = buildPosts();
